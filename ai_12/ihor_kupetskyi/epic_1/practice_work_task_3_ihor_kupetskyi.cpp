#include <cstdio>
#include <cmath>

int main() {
    double P, r, t, A;  // сума вкладу, річна ставка, роки, майбутня сума
    int choice;
    int n; // кількість нарахувань відсотків на рік

    printf("=== Розрахунок складних відсотків ===\n");

    // Ввід початкових даних
    printf("Введіть початкову суму вкладу (грн): ");
    scanf("%lf", &P);

    printf("Введіть річну відсоткову ставку (%%): ");
    scanf("%lf", &r);

    printf("Введіть термін вкладу (роки): ");
    scanf("%lf", &t);

    printf("\nВиберіть період нарахування відсотків:\n");
    printf("1 - Щомісячно\n");
    printf("2 - Щоквартально\n");
    printf("3 - Щороку\n");
    printf("Ваш вибір: ");
    scanf("%d", &choice);

    // Визначення кількості нарахувань за рік
    if (choice == 1)
        n = 12;
    else if (choice == 2)
        n = 4;
    else
        n = 1;

    // Переведення ставки у десяткову форму
    r = r / 100.0;

    // Формула складних відсотків
    A = P * pow(1 + r / n, n * t);

    double profit = A - P;

    // Вивід результатів
    printf("\n=== Результати розрахунку ===\n");
    printf("Початкова сума: %.2f грн\n", P);
    printf("Річна ставка: %.2f%%\n", r * 100);
    printf("Термін вкладу: %.1f років\n", t);

    if (choice == 1)
        printf("Нарахування: Щомісячно (n = 12)\n");
    else if (choice == 2)
        printf("Нарахування: Щоквартально (n = 4)\n");
    else
        printf("Нарахування: Щороку (n = 1)\n");

    printf("Майбутня сума (A): %.2f грн\n", A);
    printf("Заробіток (A - P): %.2f грн\n", profit);

    return 0;
}